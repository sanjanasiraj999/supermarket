    private void jb1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb1ActionPerformed
        String uzn = jt1.getText().trim();
        String pxy = jt2.getText().trim();
    
    //Check if username field is expty
    if (uzn.isEmpty()){
      JOptionPane.showMessageDialog(this,"Please enter your username.","Login",JOptionPane.WARNING_MESSAGE);
      jt1.requestFocus();
    }
    //check if password field is empty
    else if (pxy.isEmpty()){
      JOptionPane.showMessageDialog(this,"Please enter your password.","Login",JOptionPane.WARNING_MESSAGE);
      jt1.requestFocus();
    }
    //Validate correct credentials
    else if (uzn.equals("sanjana")&& pxy.equals("2003811")){
      JOptionPane.showMessageDialog(this,"login successful!.","Login",JOptionPane.WARNING_MESSAGE);
      new MainFrame().setVisible(true);
      this.dispose(); //closes login window
    }
    //Invalid credentials
    else{
        JOptionPane.showMessageDialog(this,"Invalid username or password.","Login",JOptionPane.WARNING_MESSAGE);
      jt1.setText("");
      jt2.requestFocus();
    }
    }//GEN-LAST:event_jb1ActionPerformed

